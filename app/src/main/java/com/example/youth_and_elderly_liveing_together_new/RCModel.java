package com.example.youth_and_elderly_liveing_together_new;

public class RCModel {

    private String title;
    private int image;

    public RCModel(String title, int image) {
        this.title = title;
        this.image = image;
    }

    public String getTitle() {
        return title;
    }

    public int getImage() {
        return image;
    }
}
